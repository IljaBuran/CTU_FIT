class CCell
{
protected:
    
    struct Dimension { size_t width, height; };
    
    Dimension                dimension_;
    std::vector<std::string> data_;

public:

    virtual bool operator==(const CCell& other) const = 0;
    virtual bool operator!=(const CCell& other) const = 0;
    virtual std::shared_ptr<CCell> clone()      const = 0;
    
    virtual std::vector<std::string> render(Dimension dim) = 0;

    size_t GetWidth()  const { return dimension_.width; }
    size_t GetHeight() const { return dimension_.height; }

};

class CEmpty : public CCell
{
public:

    CEmpty() { dimension_.height = dimension_.width = 0; }

    bool operator==(const CCell& other) const override
    {
        if (this == &other)
            return true;
        
        return dynamic_cast<const CEmpty*>(&other) != nullptr;
    }

    bool operator!=(const CCell& other) const override
    {
        return !(*this == other);
    }

    std::vector<std::string> render(Dimension dim) override
    {
        return std::vector<std::string>(dim.height, std::string(dim.width, ' '));
    }

    std::shared_ptr<CCell> clone() const override
    {
        return std::make_shared<CEmpty>(*this);
    }
};

class CText : public CCell
{
public:

    enum TYPE { ALIGN_LEFT, ALIGN_RIGHT };

private:

    TYPE allignment_;

public:
    
    CText(const std::string& text, CText::TYPE allignment) : allignment_(allignment) 
    {
        setText(text);
    }

    void setText(const std::string& text)
    {
        data_.clear();
        
        std::istringstream iss(text);
        std::string temp;
        size_t longestLine = 0;
        while (std::getline(iss, temp))
        {
            data_.push_back(temp);
            
            if (temp.size() > longestLine) 
                longestLine = temp.size();
        }

        dimension_.height = data_.size();
        dimension_.width = longestLine;
    }

    bool operator==(const CCell& other) const override
    {
        if (this == &other)
            return true;
        
        const CText* otherText = dynamic_cast<const CText*>(&other);

        return (otherText && data_ == otherText->data_ && allignment_== otherText->allignment_);
    }

    bool operator!=(const CCell& other) const override
    {
        return !(*this == other);
    }

    std::vector<std::string> render(Dimension dim) override
    {
        std::vector<std::string> text(dim.height, std::string(dim.width, ' '));
        for (size_t i = 0; i < data_.size() && i < dim.height; i++)
        {
            const auto& src = data_[i];
            size_t pad = allignment_ == ALIGN_LEFT ? 0 : (dim.width - src.size());
            std::copy(src.begin(), src.end(), text[i].begin() + pad);
        }
        return text;
    }

    std::shared_ptr<CCell> clone() const override
    {
        return std::make_shared<CText>(*this);
    }
};

class CImage : public CCell
{
public:
    
    CImage() 
    {
        dimension_.width  = 0;
        dimension_.height = 0;
    }

    CImage& addRow(const std::string& row)
    {
        data_.push_back(row);

        dimension_.height++;
        dimension_.width = std::max(dimension_.width, row.size());
        
        return *this;
    }

    bool operator==(const CCell& other) const override
    {
        if (this == &other)
            return true;
        
        const CImage* otherImage = dynamic_cast<const CImage*>(&other);

        return (otherImage && data_ == otherImage->data_);
    }

    bool operator!=(const CCell& other) const override 
    {
        return !(*this == other);
    }

    std::vector<std::string> render(Dimension dim) override
    {
        size_t imageRows = data_.size();
        size_t imageColumns = imageRows ? data_[0].size() : 0;
        size_t blankRows = dim.height - imageRows;
        size_t blankColumns = dim.width - imageColumns;

        std::vector<std::string> image;

        for (size_t i = 0; i < blankRows/2; i++)
            image.emplace_back(dim.width, ' ');
        
        for (const auto& line : data_)
        {
            std::string temp(dim.width, ' ');
            std::copy(line.begin(), line.end(), temp.begin() + blankColumns / 2);
            image.push_back(temp);
        }

        for (size_t i = 0; i < blankRows - blankRows / 2; i++)
            image.emplace_back(dim.width, ' ');
            
        return image;
    }

    std::shared_ptr<CCell> clone() const override
    {
        return std::make_shared<CImage>(*this);
    }
};


class CTable
{
private:

    size_t rows_;
    size_t columns_;

    std::vector<std::vector<std::shared_ptr<CCell>>> cells_;
    
public:

    CTable(size_t rows, size_t columns) 
    : rows_(rows), columns_(columns), cells_(rows, std::vector<std::shared_ptr<CCell>>(columns))
    {
        for (size_t i = 0; i < rows_; i++)
            for (size_t j = 0; j < columns_; j++)
                cells_[i][j] = std::make_shared<CEmpty>();
    }

    CTable(const CTable& other)
        : rows_(other.rows_), columns_(other.columns_), 
          cells_(other.rows_, std::vector<std::shared_ptr<CCell>>(other.columns_)) 
    {
        for (size_t i = 0; i < rows_; i++)
            for (size_t j = 0; j < columns_; j++)
                cells_[i][j] = other.cells_[i][j]->clone();
    }

    CTable& operator=(const CTable& other)
    {
        if (this != &other) 
        {
            CTable temp(other);
            std::swap(rows_, temp.rows_);
            std::swap(columns_, temp.columns_);
            std::swap(cells_, temp.cells_);
        }
        return *this;
    }
    CCell& getCell(size_t row, size_t column) const
    {
        CheckPosition(row, column);
        return *cells_[row][column];
    }

    void setCell(size_t row, size_t column, const CCell& cell)
    {
        CheckPosition(row, column);
        cells_[row][column] = cell.clone();
    }

    void setCell(size_t row, size_t column, std::shared_ptr<CCell> ptr)
    {
        CheckPosition(row, column);
        cells_[row][column] = std::move(ptr);
    }

    void setCell(size_t row, size_t column, const CTable& subTable);

    bool operator==(const CTable& other) const
    {
        if (columns_ != other.columns_ || rows_ != other.rows_)
            return false;

        for (size_t i = 0; i < rows_; i++)
            for (size_t j = 0; j < columns_; j++)
                if (*cells_[i][j] != *other.cells_[i][j])
                    return false;
        
        return true;
    }

    bool operator!=(const CTable& other) const
    {
        return !(*this == other);
    }

    friend std::ostream& operator<<(std::ostream& os, const CTable& table)
    {
        std::vector<size_t> columnSizes, rowSizes; 
        table.CalculateColumnsSize(columnSizes);
        table.CalculateRowsSize(rowSizes);
        
        table.PrintHorizontalLines(os, columnSizes);
        for (size_t i = 0; i < table.rows_; i++)
        {
            std::vector<std::vector<std::string>> rowData; 
            table.FillRowData(rowData, i, columnSizes, rowSizes);
            
            for (size_t j = 0; j < rowSizes[i]; j++)
            {
                for (size_t k = 0; k < table.columns_; k++)
                    os << '|' << rowData[k][j];

                os << "|\n";
            } 

            table.PrintHorizontalLines(os, columnSizes);
        }
        return os;
    }

private:

    inline void CheckPosition(size_t row, size_t column) const
    {
        if (row >= rows_ || column >= columns_)
            throw std::invalid_argument("Invalid position argument");
    }

    void CalculateColumnsSize(std::vector<size_t>& columnSizes) const
    {
        columnSizes.assign(columns_, 0);
        for (size_t i = 0; i < columns_; i++)
            for (size_t j = 0; j < rows_; j++)
                columnSizes[i] = std::max(columnSizes[i], cells_[j][i]->GetWidth());
    }

    void CalculateRowsSize(std::vector<size_t>& rowSizes) const
    {
        rowSizes.assign(rows_, 0);
        for (size_t i = 0; i < rows_; i++)
            for (size_t j = 0; j < columns_; j++)
                rowSizes[i] = std::max(rowSizes[i], cells_[i][j]->GetHeight());
    }

    void PrintHorizontalLines(std::ostream& os, const std::vector<size_t> columnSizes) const
    {
        for (auto cSize : columnSizes)
            os << '+' << std::string(cSize, '-');
        os << "+\n";
    }

    void FillRowData(std::vector<std::vector<std::string>>& data, 
                     size_t                                 row,
                     const std::vector<size_t>              columnSizes,
                     const std::vector<size_t>              rowSizes) const
    {
        for (size_t i = 0; i < columns_; i++)
            data.push_back(cells_[row][i]->render({columnSizes[i], rowSizes[row]}));
    }
};

class CTableCell : public CCell
{
private:

    CTable table_;

public:
    
    CTableCell(const CTable& table) : table_(table)
    {
        std::ostringstream oss;
        oss << table_;
        std::istringstream iss(oss.str());

        std::string temp;
        std::vector<std::string> lines;

        while (std::getline(iss, temp)) 
            lines.push_back(temp);
        
        dimension_.height = lines.size();
        dimension_.width  = lines.empty() ? 0 : lines[0].size();
    }

    bool operator==(const CCell& other) const override
    {
        if (this == &other)
            return true;
        
        const CTableCell* otherCell = dynamic_cast<const CTableCell*>(&other);
            
        return otherCell && table_ == otherCell->table_;
    }

    bool operator!=(const CCell& other) const override 
    { 
        return !(*this == other); 
    }

    std::vector<std::string> render(Dimension dim) override
    {
        std::ostringstream oss;
        oss << table_;
        std::istringstream iss(oss.str());

        std::vector<std::string> lines;
        std::string temp;
        while (getline(iss, temp)) 
            lines.push_back(temp);

        std::vector<std::string> out(dim.height, std::string(dim.width, ' '));
        for (size_t i = 0; i < lines.size(); i++) 
        {
            std::string& dest = out[i];
            std::string& src = lines[i];
            dest.replace(0, src.size(), src);
        }
        return out;
    }

    std::shared_ptr<CCell> clone() const override
    {
        return std::shared_ptr<CCell>(new CTableCell(*this));
    }
};

void CTable::setCell(size_t row, size_t column, const CTable &subTable)
{
    cells_[row][column].reset(new CTableCell(subTable));
}


